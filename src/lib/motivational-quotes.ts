const tiers = [tier1, tier2, tier3, tier4, tier5];

function simpleHash(str: string): number {
  let h = 0;
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) - h + str.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

export function getMotivationalQuote(salary: number, recordId: string): string {
  let tierIndex: number;
  if (salary < 1000) tierIndex = 0;
  else if (salary < 3000) tierIndex = 1;
  else if (salary < 5000) tierIndex = 2;
  else if (salary < 8000) tierIndex = 3;
  else tierIndex = 4;

  const quotes = tiers[tierIndex];
  const idx = simpleHash(recordId) % quotes.length;
  return quotes[idx];
}
